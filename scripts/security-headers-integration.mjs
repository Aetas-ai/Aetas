import { createHash } from 'node:crypto';
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const hashSource = (value) =>
  `'sha256-${createHash('sha256').update(value, 'utf8').digest('base64')}'`;

async function collectFiles(directory, extension) {
  const files = [];
  const entries = await readdir(directory, { withFileTypes: true });

  for (const entry of entries) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...await collectFiles(entryPath, extension));
    } else if (entry.isFile() && entry.name.endsWith(extension)) {
      files.push(entryPath);
    }
  }

  return files;
}

function validatePublicArtifacts(files, outputDirectory) {
  const forbidden = files.filter((file) => {
    const name = path.basename(file).toLowerCase();
    return name.endsWith('.map')
      || name.endsWith('.log')
      || name.endsWith('.pem')
      || name.endsWith('.key')
      || name.endsWith('.p12')
      || name.endsWith('.pfx')
      || name.endsWith('.jks')
      || name.endsWith('.keystore')
      || name === '.env'
      || name.startsWith('.env.')
      || name === '.gitignore'
      || name === '.npmrc'
      || name === '.netrc'
      || /(?:^|[-_.])(?:credential|credentials|secret|secrets|service-account)(?:[-_.]|$)/.test(name);
  });

  if (forbidden.length > 0) {
    const relativeFiles = forbidden.map((file) => path.relative(outputDirectory, file));
    throw new Error(`public build contains forbidden artifacts: ${relativeFiles.join(', ')}`);
  }
}

function collectInlineHashes(html, tagName) {
  const hashes = [];
  const expression = new RegExp(`<${tagName}\\b([^>]*)>([\\s\\S]*?)<\\/${tagName}>`, 'gi');

  for (const match of html.matchAll(expression)) {
    const attributes = match[1];
    const content = match[2];
    if (tagName === 'script' && /\bsrc\s*=/i.test(attributes)) continue;
    if (content.length > 0) hashes.push(hashSource(content));
  }

  return hashes;
}

function validateGeneratedHtml(html, filename) {
  const violations = [];

  for (const match of html.matchAll(/<a\b([^>]*)>/gi)) {
    const attributes = match[1];
    if (!/\btarget\s*=\s*(["'])_blank\1/i.test(attributes)) continue;

    const rel = attributes.match(/\brel\s*=\s*(["'])(.*?)\1/i)?.[2]
      .toLowerCase()
      .split(/\s+/) ?? [];

    if (!rel.includes('noopener') || !rel.includes('noreferrer')) {
      violations.push('target="_blank" link without rel="noopener noreferrer"');
    }
  }

  if (/\son[a-z]+\s*=/i.test(html)) {
    violations.push('inline event handler blocked by script-src-attr');
  }

  if (violations.length > 0) {
    throw new Error(`${filename}: ${violations.join('; ')}`);
  }
}

function createContentSecurityPolicy(scriptHashes, styleHashes) {
  // Framer Motion and GSAP animate by updating element.style every frame. CSP
  // governs those mutations through style-src-attr, so this narrowly scoped
  // exception is required to preserve the existing motion system. Inline
  // <style> elements remain restricted to the exact build-generated hashes.
  const directives = [
    "default-src 'self'",
    "base-uri 'self'",
    "object-src 'none'",
    "frame-ancestors 'none'",
    "form-action 'self'",
    `script-src 'self' 'wasm-unsafe-eval' ${scriptHashes.join(' ')}`,
    "script-src-attr 'none'",
    `style-src 'self' https://fonts.googleapis.com ${styleHashes.join(' ')}`,
    "style-src-attr 'unsafe-inline'",
    "font-src 'self' data: https://fonts.gstatic.com",
    "img-src 'self' data: blob: https://uploads-ssl.webflow.com https://*.website-files.com",
    "connect-src 'self' https://fonts.googleapis.com https://fonts.gstatic.com https://unpkg.com https://api.webflow.com https://api-cdn.webflow.com",
    "worker-src 'self' blob:",
    "child-src 'self' blob:",
    "media-src 'self' blob: https://uploads-ssl.webflow.com https://*.website-files.com",
    "frame-src 'self'",
    "manifest-src 'self'",
    'upgrade-insecure-requests',
  ];

  return directives.join('; ');
}

function createHtaccess(contentSecurityPolicy) {
  const permissionsPolicy = [
    'accelerometer=()',
    'autoplay=(self)',
    'camera=()',
    'display-capture=()',
    'encrypted-media=()',
    'fullscreen=(self)',
    'geolocation=()',
    'gyroscope=()',
    'magnetometer=()',
    'microphone=()',
    'midi=()',
    'payment=()',
    'picture-in-picture=(self)',
    'publickey-credentials-get=()',
    'screen-wake-lock=()',
    'usb=()',
    'web-share=(self)',
    'xr-spatial-tracking=()',
  ].join(', ');

  return `# Generated by scripts/security-headers-integration.mjs during astro build.
# Do not edit the generated .htaccess directly; update the integration instead.
Options -Indexes

<IfModule mod_rewrite.c>
  RewriteEngine On

  # Use a fixed redirect destination so an untrusted Host header cannot create
  # an open redirect. The canonical host rule also handles the www hostname.
  RewriteCond %{HTTP_HOST} !^aetas\\.ai(?::443)?$ [NC]
  RewriteRule ^ https://aetas.ai%{REQUEST_URI} [R=308,L]

  # Hostinger may terminate TLS before LiteSpeed and forward the original
  # protocol. Redirect only when neither layer reports an HTTPS request.
  RewriteCond %{HTTPS} !=on
  RewriteCond %{HTTP:X-Forwarded-Proto} !^https$ [NC]
  RewriteRule ^ https://aetas.ai%{REQUEST_URI} [R=308,L]
</IfModule>

<IfModule mod_headers.c>
  Header always set Content-Security-Policy "${contentSecurityPolicy}"
  Header always set Strict-Transport-Security "max-age=31536000"
  Header always set X-Content-Type-Options "nosniff"
  Header always set Referrer-Policy "strict-origin-when-cross-origin"
  Header always set Permissions-Policy "${permissionsPolicy}"
  Header always set X-Frame-Options "DENY"

  # Public source assets can change without a filename change, so keep their
  # cache short. Astro's /_astro/ output is content-hashed and safe to retain.
  <FilesMatch "\\.(?:avif|css|gif|ico|jpe?g|js|png|svg|webp|woff2?)$">
    Header always set Cache-Control "public, max-age=3600, must-revalidate"
  </FilesMatch>
  <FilesMatch "\\.(?:html?|xml|txt)$">
    Header always set Cache-Control "public, max-age=0, must-revalidate"
  </FilesMatch>
  <IfModule mod_setenvif.c>
    SetEnvIf Request_URI "^/_astro/" aetas_fingerprinted_asset
    Header always set Cache-Control "public, max-age=31536000, immutable" env=aetas_fingerprinted_asset

    SetEnvIf Request_URI "^/(?:api|admin|cms-preview|preview)(?:/|$)" aetas_sensitive_route
    Header always set Cache-Control "private, no-store, max-age=0" env=aetas_sensitive_route
    Header always set Pragma "no-cache" env=aetas_sensitive_route
    Header always set X-Robots-Tag "noindex, nofollow, noarchive" env=aetas_sensitive_route
  </IfModule>
</IfModule>

<FilesMatch "(?i)(?:\\.(?:map|log|pem|key|p12|pfx|jks|keystore)$|^\\.env(?:\\.|$)|^\\.git|^\\.npmrc$|^\\.netrc$|(?:^|[-_.])(?:credentials?|secrets?|service-account)(?:[-_.]|$))">
  Require all denied
</FilesMatch>
`;
}

export default function securityHeaders() {
  return {
    name: 'aetas-security-headers',
    hooks: {
      'astro:build:done': async ({ dir, logger }) => {
        const outputDirectory = fileURLToPath(dir);
        const publicFiles = await collectFiles(outputDirectory, '');
        validatePublicArtifacts(publicFiles, outputDirectory);
        const htmlFiles = await collectFiles(outputDirectory, '.html');
        const scriptHashes = new Set();
        const styleHashes = new Set();

        for (const htmlFile of htmlFiles) {
          const html = await readFile(htmlFile, 'utf8');
          validateGeneratedHtml(html, path.relative(outputDirectory, htmlFile));
          collectInlineHashes(html, 'script').forEach((hash) => scriptHashes.add(hash));
          collectInlineHashes(html, 'style').forEach((hash) => styleHashes.add(hash));
        }

        const sortedScriptHashes = [...scriptHashes].sort();
        const sortedStyleHashes = [...styleHashes].sort();
        const contentSecurityPolicy = createContentSecurityPolicy(
          sortedScriptHashes,
          sortedStyleHashes,
        );
        const htaccessPath = path.join(outputDirectory, '.htaccess');

        await writeFile(htaccessPath, createHtaccess(contentSecurityPolicy), 'utf8');
        logger.info(
          `generated .htaccess with ${sortedScriptHashes.length} script hashes and ${sortedStyleHashes.length} style hashes`,
        );
      },
    },
  };
}
